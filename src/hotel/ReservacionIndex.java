    public void misReservaciones(){
        ArrayList<Reservacion> lista = new ArrayList();
        for(Reservacion reser : Hotel.listaReservaciones){
            if(Hotel.user.getId()==reser.getId_cliente()){
                lista.add(reser);
            }
        }
        Hotel.listaReservaciones = lista;
    }
    

