    public void buscar(int id){
        String array[] = new String[7];
        
        for(Reservacion reserv : Hotel.listaReservaciones){
            if(id==reserv.getId()){
                array[0] = String.valueOf(reserv.getId());
                array[1] = String.valueOf(reserv.getId_cliente());
                array[2] = String.valueOf(reserv.getId_habitacion());
                array[3] = fechaF.format(reserv.getFechaReservacion());
                array[4] = reserv.getHoraReservacion();
                array[5] = fechaF.format(reserv.getFechaEntrada());
                array[6] = fechaF.format(reserv.getFechaSalida());
                modelo.addRow(array);
                return;
            }
        }
        JOptionPane.showMessageDialog(null, "No se hayaron resultados");
    }
    
